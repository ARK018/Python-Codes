# Program to perform tuple operations in python
tuple1 = (1, 2, 3, 4, 5)
tuple2 = ('h', 'e', 'l', 'l', 'o')

print(f"Maximum value in tuple1 : {max(tuple1)}")
print(f"Minimum value in tuple1 : {min(tuple1)}")
print(f"Maximum value in tuple2 : {max(tuple2)}")
print(f"Minimum value in tuple2 : {min(tuple2)}")

